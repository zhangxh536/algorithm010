/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public ListNode reverseList(ListNode head) {
        ListNode pre = null;
        ListNode temp;
        while (head != null) {
            temp = head.next;//temp指向下一个节点并保存
            head.next = pre;//把指针反转
            pre = head;
            head = temp;
        }
        return pre;
    }
}
